package com.zb.entity;

public class Class {
    private String id;
    private String className;

    public void setId(String id){
        this.id = id;
    }
    public String getId(){
        return id;
    }

    public void setClassName(String className){
        this.className = className;
    }
    public String getClassName(){
        return className;
    }

}
